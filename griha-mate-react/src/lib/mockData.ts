// Helper function to get paginated results
export const getPaginatedProperties = (
  properties: PropertyDto[],
  page: number = 1,
  pageSize: number = 12
): { data: PropertyDto[]; total: number; totalPages: number; currentPage: number } => {
  const start = (page - 1) * pageSize
  const end = start + pageSize
  const paginatedData = properties.slice(start, end)
  
  return {
    data: paginatedData,
    total: properties.length,
    totalPages: Math.ceil(properties.length / pageSize),
    currentPage: page
  }
}


